    # ── Campaign Management ───────────────────────────────────────────────────

    async def list_campaigns(
        self, business_id: UUID, campaign_type: str | None = None, limit: int = 50, offset: int = 0
    ) -> list[MarketingCampaign]:
        query = (
            select(MarketingCampaign)
            .where(
                MarketingCampaign.business_id == business_id,
                MarketingCampaign.parent_campaign_id.is_(None),
            )
            .order_by(MarketingCampaign.created_at.desc())
            .limit(limit)
            .offset(offset)
        )
        if campaign_type:
            query = query.where(MarketingCampaign.campaign_type == campaign_type)
        result = await self.db.execute(query)
        campaigns = list(result.scalars().all())
        for campaign in campaigns:
            campaign.metrics = await self.real_campaign_metrics(campaign.id)
        return campaigns

    async def real_campaign_metrics(self, campaign_id: UUID) -> dict:
        result = await self.db.execute(
            select(
                AnalyticsEvent.event_type,
                func.count(AnalyticsEvent.id),
                func.coalesce(func.sum(AnalyticsEvent.value_cents), 0),
            )
            .where(AnalyticsEvent.campaign_id == campaign_id)
            .group_by(AnalyticsEvent.event_type)
        )
        clicks = conversions = opens = sent = 0
        revenue_cents = 0
        for event_type, count, value in result.all():
            if event_type == "click":
                clicks = count or 0
            elif event_type == "conversion":
                conversions = count or 0
                revenue_cents += int(value or 0)
            elif event_type == "email_open":
                opens = count or 0
            elif event_type in {"email_sent", "sent"}:
                sent = count or 0
        return {
            "impressions": 0,
            "clicks": clicks,
            "conversions": conversions,
            "opens": opens,
            "sent": sent,
            "revenue_cents": revenue_cents,
            "ctr": 0,
            "roas": 0,
            "analytics_source": "real",
        }

    async def approve_campaign(
        self, campaign_id: UUID, approved_by: str
    ) -> MarketingCampaign | None:
        """Approve a draft or pending campaign. Sending/publishing remains explicit."""
        campaign = await self.db.get(MarketingCampaign, campaign_id)
        if not campaign:
            return None
        current_status = str(campaign.status or "").lower()
        lifecycle_status = str(campaign.lifecycle_status or "").lower()
        approvable = {"draft", "pending_approval", "pending approval", "rejected", "approved"}
        if current_status not in approvable and lifecycle_status not in approvable:
            return None
        campaign.status = "approved"
        campaign.lifecycle_status = "approved"
        campaign.approved_by = approved_by
        await self.db.commit()
        logger.info("Campaign approved  id=%s  by=%s", campaign_id, approved_by)
        return campaign

    async def reject_campaign(
        self, campaign_id: UUID, reason: str
    ) -> MarketingCampaign | None:
        campaign = await self.db.get(MarketingCampaign, campaign_id)
        if not campaign:
            return None
        campaign.status = "rejected"
        campaign.rejection_reason = reason
        await self.db.commit()
        return campaign

    async def update_campaign_content(
        self,
        campaign_id: UUID,
        *,
        name: str | None = None,
        content: dict | None = None,
        content_text: str | None = None,
    ) -> MarketingCampaign | None:
        campaign = await self.db.get(MarketingCampaign, campaign_id)
        if not campaign:
            return None
        if name is not None and name.strip():
            campaign.name = name.strip()[:200]
        if content is not None:
            campaign.content = content
        elif content_text is not None:
            campaign.content = self._with_primary_text(campaign.content or {}, content_text)
        await self.db.commit()
        await self.db.refresh(campaign)
        return campaign

    @staticmethod
    def _with_primary_text(content: dict, text: str) -> dict:
        next_content = dict(content or {})
        if isinstance(next_content.get("posts"), list) and next_content["posts"]:
            first = dict(next_content["posts"][0] or {})
            first["text"] = text
            next_content["posts"] = [first, *next_content["posts"][1:]]
        elif "plain_text_body" in next_content or "html_body" in next_content:
            next_content["plain_text_body"] = text
            next_content["body"] = text
        elif "content_markdown" in next_content:
            next_content["content_markdown"] = text
        elif "post_text" in next_content:
            next_content["post_text"] = text
        elif "body" in next_content:
            next_content["body"] = text
        elif "headline" in next_content:
            next_content["headline"] = text
        else:
            next_content["body"] = text
        return next_content

