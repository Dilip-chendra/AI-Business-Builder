    # ── Campaign Management ───────────────────────────────────────────────────

    async def list_campaigns(
        self, business_id: UUID, campaign_type: str | None = None, limit: int = 50, offset: int = 0
    ) -> list[MarketingCampaign]:
        query = (
            select(MarketingCampaign)
            .where(MarketingCampaign.business_id == business_id)
            .order_by(MarketingCampaign.created_at.desc())
            .limit(limit)
            .offset(offset)
        )
        if campaign_type:
            query = query.where(MarketingCampaign.campaign_type == campaign_type)
        result = await self.db.execute(query)
        return list(result.scalars().all())

    async def approve_campaign(
        self, campaign_id: UUID, approved_by: str
    ) -> MarketingCampaign | None:
        """Approve a pending campaign. For email: triggers send. For ads: marks ready."""
        campaign = await self.db.get(MarketingCampaign, campaign_id)
        if not campaign or campaign.status != "pending_approval":
            return None
        campaign.status = "approved"
        campaign.approved_by = approved_by
        await self.db.commit()
        logger.info("Campaign approved  id=%s  by=%s", campaign_id, approved_by)
        return campaign

    async def reject_campaign(
        self, campaign_id: UUID, reason: str
    ) -> MarketingCampaign | None:
        campaign = await self.db.get(MarketingCampaign, campaign_id)
        if not campaign:
            return None
        campaign.status = "rejected"
        campaign.rejection_reason = reason
        await self.db.commit()
        return campaign

    async def duplicate_campaign(self, campaign_id: UUID) -> MarketingCampaign | None:
        campaign = await self.db.get(MarketingCampaign, campaign_id)
        if not campaign:
            return None
        new_campaign = MarketingCampaign(
            business_id=campaign.business_id,
            campaign_type=campaign.campaign_type,
            name=f"Copy of {campaign.name}",
            status="draft",
            content=campaign.content,
            targeting=campaign.targeting,
            metrics={"impressions": 0, "clicks": 0, "conversions": 0, "spend_cents": 0},
        )
        self.db.add(new_campaign)
        await self.db.commit()
        await self.db.refresh(new_campaign)
        return new_campaign

    async def send_approved_email_campaign(
        self,
        campaign_id: UUID,
        recipient_emails: list[str],
        user_id: str,
    ) -> dict:
        """Send an approved email campaign to a list of recipients."""
        campaign = await self.db.get(MarketingCampaign, campaign_id)
        if not campaign:
            return {"error": "Campaign not found"}
        if campaign.status != "approved":
            return {"error": f"Campaign is not approved (status: {campaign.status})"}
        if campaign.campaign_type != "email":
            return {"error": "Only email campaigns can be sent via this method"}

        from app.services.email_service import EmailService
        svc = EmailService()
        content = campaign.content
        sent = 0
        failed = 0

        for email in recipient_emails:
            success = await svc.send_marketing_campaign(
                user_id=user_id,
                to=email,
                subject=content.get("subject", "Message from us"),
                html_body=content.get("html_body", ""),
                db=self.db,
            )
            if success:
                sent += 1
            else:
                failed += 1

        campaign.status = "sent"
        campaign.metrics = {"sent": sent, "failed": failed, "total": len(recipient_emails)}
        await self.db.commit()
        logger.info("Email campaign sent  id=%s  sent=%d  failed=%d", campaign_id, sent, failed)
        return {"sent": sent, "failed": failed}

