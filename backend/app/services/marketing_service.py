"""Marketing Engine — real AI-generated campaigns, SEO content, and ad payloads.

All content is generated by the real AI provider (Ollama/Groq/HF).
No dummy data. If AI is unavailable, raises AIProviderError.

Approval workflow:
- Email campaigns → status="pending_approval" until user approves
- Ad campaigns → status="pending_approval" always (never auto-launch)
- SEO blogs → status="draft", user publishes manually

Uses SmartAIOrchestrator for intelligent provider selection and retry strategy.
"""
from __future__ import annotations

import logging
import re
from uuid import UUID

from sqlalchemy import select
from sqlalchemy.ext.asyncio import AsyncSession

from app.models.marketing import MarketingCampaign, SeoContent
from app.services.ai_orchestrator import generate_json_smart, generate_text_smart, ProviderChoice

logger = logging.getLogger(__name__)


class MarketingService:
    def __init__(self, db: AsyncSession) -> None:
        self.db = db

    # ── SEO Content ───────────────────────────────────────────────────────────

    async def generate_seo_blog(
        self,
        business_id: UUID,
        topic: str,
        target_keyword: str,
        business_context: dict,
    ) -> SeoContent:
        """Generate a full SEO blog post using real AI with smart provider selection."""
        prompt = (
            "You are an expert SEO content writer.\n"
            "Write a complete, high-quality SEO blog post.\n"
            "Return ONLY valid JSON with these keys:\n"
            "  title, slug, meta_description, content_markdown, keywords (array of strings)\n\n"
            f"Business: {business_context.get('name')}\n"
            f"Niche: {business_context.get('niche')}\n"
            f"Target audience: {business_context.get('target_audience')}\n"
            f"Topic: {topic}\n"
            f"Primary keyword: {target_keyword}\n\n"
            "Requirements:\n"
            "- Title: compelling, includes keyword, under 60 chars\n"
            "- Slug: URL-friendly, lowercase, hyphens\n"
            "- Meta description: 150-160 chars, includes keyword\n"
            "- Content: 800-1200 words, markdown format, H2/H3 headings, practical value\n"
            "- Keywords: 5-8 related keywords"
        )
        # Use smart orchestrator for intelligent provider selection
        data = await generate_json_smart(prompt, prefer_provider=ProviderChoice.AUTO, task_type="marketing")
        content_md = data.get("content_markdown", "")
        word_count = len(content_md.split())

        seo = SeoContent(
            business_id=business_id,
            title=data.get("title", topic),
            slug=data.get("slug", re.sub(r"[^a-z0-9]+", "-", topic.lower())),
            meta_description=data.get("meta_description", ""),
            content_markdown=content_md,
            keywords=data.get("keywords", [target_keyword]),
            status="draft",
            word_count=word_count,
        )
        self.db.add(seo)
        await self.db.commit()
        await self.db.refresh(seo)
        logger.info("SEO blog generated  business_id=%s  title=%r", business_id, seo.title)
        return seo

    async def list_seo_content(self, business_id: UUID) -> list[SeoContent]:
        result = await self.db.execute(
            select(SeoContent)
            .where(SeoContent.business_id == business_id)
            .order_by(SeoContent.created_at.desc())
        )
        return list(result.scalars().all())

    async def publish_seo_content(self, content_id: UUID) -> SeoContent | None:
        content = await self.db.get(SeoContent, content_id)
        if not content:
            return None
        content.status = "published"
        await self.db.commit()
        return content

    # ── Email Campaigns ───────────────────────────────────────────────────────

    async def generate_email_campaign(
        self,
        business_id: UUID,
        campaign_name: str,
        goal: str,
        business_context: dict,
        product_context: dict | None = None,
        project_id: UUID | None = None,
        product_id: UUID | None = None,
        recipient_count: int = 0,
    ) -> MarketingCampaign:
        """Generate an email campaign. Requires human approval before sending."""
        product_block = ""
        if product_context:
            product_block = (
                f"Featured product: {product_context.get('name')}\n"
                f"Product description: {product_context.get('description')}\n"
                f"Product price: {product_context.get('price')} {product_context.get('currency', 'USD')}\n"
                f"Product type: {product_context.get('product_type', 'digital')}\n"
            )
        prompt = (
            "You are an expert email marketer.\n"
            "Generate a high-converting email campaign.\n"
            "Return ONLY valid JSON with these keys:\n"
            "  subject, preview_text, html_body, plain_text_body, cta_text, cta_url_placeholder\n\n"
            f"Business: {business_context.get('name')}\n"
            f"Niche: {business_context.get('niche')}\n"
            f"Brand tone: {business_context.get('brand_tone', 'professional')}\n"
            f"Target audience: {business_context.get('target_audience')}\n"
            f"{product_block}"
            f"Campaign goal: {goal}\n\n"
            "Requirements:\n"
            "- Subject: compelling, under 50 chars, no spam words\n"
            "- Preview text: 90-100 chars\n"
            "- HTML body: clean HTML email, mobile-friendly\n"
            "- Plain text: fallback version\n"
            "- CTA: clear action button text"
        )
        data = await generate_json_smart(prompt, prefer_provider=ProviderChoice.AUTO, task_type="marketing")

        campaign = MarketingCampaign(
            business_id=business_id,
            project_id=project_id,
            product_id=product_id,
            campaign_type="email",
            name=campaign_name,
            status="pending_approval",  # ALWAYS requires approval
            content=data,
            targeting={"recipient_count": recipient_count, "goal": goal},
            metrics={},
        )
        self.db.add(campaign)
        await self.db.commit()
        await self.db.refresh(campaign)
        logger.info(
            "Email campaign generated  business_id=%s  name=%r  status=pending_approval",
            business_id, campaign_name,
        )
        return campaign

