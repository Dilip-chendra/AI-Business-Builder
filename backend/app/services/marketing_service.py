    async def send_approved_email_campaign(
        self,
        campaign_id: UUID,
        recipient_emails: list[str],
        user_id: str,
    ) -> dict:
        """Send an approved email campaign to a list of recipients."""
        campaign = await self.db.get(MarketingCampaign, campaign_id)
        if not campaign:
            return {"error": "Campaign not found"}
        if campaign.status != "approved":
            return {"error": f"Campaign is not approved (status: {campaign.status})"}
        if campaign.campaign_type != "email":
            return {"error": "Only email campaigns can be sent via this method"}

        from app.services.email_service import EmailService
        svc = EmailService()
        content = campaign.content
        sent = 0
        failed = 0

        from datetime import datetime, timezone

        for email in recipient_emails:
            contact = await self._get_or_create_contact(campaign.business_id, email)
            recipient = CampaignRecipient(
                campaign_id=campaign.id,
                contact_id=contact.id if contact else None,
                email=email.strip().lower(),
                status="sending",
            )
            self.db.add(recipient)
            await self.db.flush()
            success = await svc.send_marketing_campaign(
                user_id=user_id,
                to=email,
                subject=content.get("subject", "Message from us"),
                html_body=content.get("html_body", ""),
                db=self.db,
            )
            if success:
                sent += 1
                recipient.status = "sent"
                recipient.sent_at = datetime.now(timezone.utc)
                self.db.add(
                    AnalyticsEvent(
                        business_id=campaign.business_id,
                        product_id=campaign.product_id,
                        campaign_id=campaign.id,
                        contact_id=recipient.contact_id,
                        event_type="email_sent",
                        source="email_provider",
                        value_cents=0,
                        metadata_json={"recipient_id": str(recipient.id), "email": recipient.email},
                    )
                )
            else:
                failed += 1
                recipient.status = "failed"

        if sent > 0:
            campaign.status = "sent"
            campaign.lifecycle_status = "sent"
        else:
            campaign.status = "approved"
            campaign.lifecycle_status = "approved"
        campaign.metrics = {
            "sent": sent,
            "failed": failed,
            "total": len(recipient_emails),
            "clicks": 0,
            "conversions": 0,
            "revenue_cents": 0,
            "analytics_source": "real",
        }
        await self.db.commit()
        logger.info("Email campaign sent  id=%s  sent=%d  failed=%d", campaign_id, sent, failed)
        if sent == 0:
            return {
                "error": (
                    "Email provider did not send any recipients. Configure a verified sender/API key "
                    "or connect Gmail before retrying."
                ),
                "sent": sent,
                "failed": failed,
            }
        return {"sent": sent, "failed": failed}

    async def _get_or_create_contact(self, business_id: UUID, email: str) -> Contact | None:
        normalized = email.strip().lower()
        if not normalized:
            return None
        result = await self.db.execute(
            select(Contact).where(Contact.business_id == business_id, Contact.email == normalized)
        )
        contact = result.scalar_one_or_none()
        if contact:
            return contact
        contact = Contact(
            business_id=business_id,
            email=normalized,
            source="email_campaign",
            consent_status="unknown",
        )
        self.db.add(contact)
        await self.db.flush()
        return contact

